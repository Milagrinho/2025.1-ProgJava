public class TiposDeDados {
    //psvm + TAB ==> main
    public static void main(String[] args) {
        //Comentario de linha
        /*
        Comentario 
        de bloco
        */
        
        //Tipos de dados
        //int, float, string, boolean ==> python
        //int, float, char, bool ==> C
        
        //Tipos inteiros
        byte a = (byte) 129;        //1 byte
        System.out.println(a);
        short b = 3652;             //2 byte
        int c = 400000000;              //4 bytes
        long d = 4000000000000000000L;          //8 bytes
        
        //Tipos reais
        float e = 10000.e12f;            //4 bytes
        double f = 10000.0;          //8 bytes
        
        //Caracteres
        char g = 'a';
        String nome = "Angela";
    }
}
