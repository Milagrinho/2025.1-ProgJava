public class Repeticao {
    public static void main(String[] args) {
        //for
        //while
        //do while
        
        for(int i = 0; i < 10; i++){
            System.out.println(i);
        }
        
        int x = 0;        
        while(x < 10){
            System.out.println(x);
            x++;
        }
        
        int y = 0;
        do{
            System.out.println(y);
            y++;
        }while(y < 10);
    }
}
